package in.at.classandobject

class DataTypesDemo {

    static main(args){

        //Java-DataTypes

        byte byteVariable = 100
        println "Byte value is: "+byteVariable

        short shortVariable = 1000
        println "Short value is: "+shortVariable

        int intVaribale = 10000
        println("Integer value is: "+intVaribale)

        long longVaribale = 10000000L
        println("Long value is: "+longVaribale)

        float floatVariable = 20.1f
        println("Float value is: "+floatVariable)

        double doubleVariable = 60.33
        println("Double value is: "+doubleVariable)

        boolean booleanVariable = true
        println("Boolean value is: "+booleanVariable)

        char charVariable = 100
        println("Character value is: "+charVariable)
    }
}
